        <h1> 5 "Silent" Killers of Cats</h1>
        <img src="ca.jpg" alt="ca" align="right" height="350px" width="450px">
        <P  style="font-size:25px;"> 

        
             As cat guardians, you should also be aware of five “silent” killers in cats. By knowing what the most common silent killers are, you can know what clinical signs to look for. With most of these diseases, the sooner the clinical signs are recognized, the sooner we veterinarians can treat.<br />
            
            1. Chronic kidney disease<br />
            One of the top silent killers of cats is chronic kidney disease (CKD) (This is sometimes called chronic renal failure or chronic kidney injury). These terms are all semantically the same, and basically mean that 75% of both the kidneys are ineffective and not working. Clinical signs of CRD include:<br />
            
            Excessive drinking<br/>
            Excessive urinating<br/>
            Larger clumps in the litter box<br/>
            Weight loss<br/>
            Bad breath (due to toxins building up in the blood and causing ulcers in the mouth, esophagus, and stomach)<br/>
            Lethargy<br/>
            Hiding<br/>
            Thankfully, with appropriate management, cats can live with CKD for years (unlike dogs where CKD usually progresses more rapidly). Chronic management may include a low-protein diet, frequent blood work, increasing water intake (e.g., with a water fountain or by feeding a grueled canned food), medications and even fluids under the skin (which many pet guardians do at home, once properly trained).<br/>
            
            
            <br/> 2. Hyperthyroidism<br/>
            Hyperthyroidism is an endocrine disease where the thyroid gland produces too much thyroid hormone. This is seen in middle-aged to geriatric cats, and can result in very similar clinical signs to chronic kidney disease including:<br/>
            
            Excessive thirst<br/>
            Increased water consumption/urination<br/>
            Vomiting/diarrhea<br/>
            Weight loss<br/>
            However, as hyperthyroidism increases the metabolism of cats, it causes one defining sign: a ravenous appetite despite weight loss. It can also result in:<br/>
            
            A racing heart rate<br/>
            Severe hypertension (resulting in acute blood loss, neurologic signs, or even a clot or stroke)<br/>
            Secondary organ injury (e.g., a heart murmur or changes to the kidney)<br/>
            Thankfully, treatment for hyperthyroidism is very effective and includes either a medication (called methimazole, surgical removal of the thyroid glands (less commonly done), a special prescription diet called y/d® Feline Thyroid Health), or I131 radioiodine therapy. With hyperthyroidism, the sooner you treat it, the less potential side effects or organ damage will occur in your cat.<br/>
            
            
            
            <br/>3. Diabetes mellitus
            Another costly, silent killer that affects cats is diabetes mellitus (DM). As many of our cats are often overweight to obese, they are at a greater risk for DM. With diabetes, the pancreas fails to secrete adequate amounts of insulin (Type I DM) or there is resistance to insulin (Type II DM). Insulin is a natural hormone that drives sugar (i.e., blood glucose) into the cells. As a result of the cells starving for glucose, the body makes more and more glucose, causing hyperglycemia (i.e., a high blood sugar) and many of the clinical signs seen with DM. Common clinical signs for DM are similar to those of Chronic kidney disease and hyperthyroidism and include:
            
            Excessive urination and thirst<br/>
            Larger clumps in the litter box<br/>
            An overweight or obese body condition with muscle wasting (especially over the spine or back) or weight loss
            A decreased or ravenous appetite<br/>
            Lethargy or weakness<br/>
            Vomiting<br/>
            Abnormal breath (e.g., acetone breath)<br/>
            Walking abnormally (e.g., lower to the ground)<br/>
            Treatment for DM can be costly, as it requires twice-a-day insulin injections that you have to give under the skin. It also requires changes in diet (to a high protein, low carbohydrate diet), frequent blood glucose monitoring, and frequent veterinary visits. With supportive care and chronic management, cats can do reasonably well; however, once diabetic complications develop (e.g., diabetic ketoacidosis, hyperosmolar, hyperglycemic syndrome), DM can be life threatening. <br/>
            
            4. Cardiac disease
            Heart disease is very frustrating for both cat owners and veterinarians. That’s because, while dogs almost always have a loud heart murmur (i.e., one we can hear with our stethoscope) indicative of heart disease, cats often don’t have a heart murmur present. In fact, it’s estimated that 50% of cats with heart disease have no auscultable heart murmur. Clinical signs of heart disease include:<br/>
            
            A heart murmur<br/>
            An abnormal heart rhythm (e.g., an abnormal beat and rhythm)<br/>
            A racing heart rate<br/>
            Collapse<br/>
            Passing out (e.g., syncope)<br/>
            Increased respiratory rate<br/>
            Difficulty breathing<br/>
            Blue-tinged gums<br/>
            Open mouth breathing<br/>
            Acute, sudden paralysis (e.g., typically of the hind limbs)<br/>
            Cold, painful hind limbs<br/>
            Sudden pain<br/>
            Sudden lameness<br/>
            Sudden death<br/>
            Once cardiac disease is diagnosed (typically based on physical exam, chest radiographs, Cardiopet® proBNP Test, and an ultrasound of the heart called an “echocardiogram”), treatment may include emergency care for oxygen therapy, diuretics, blood pressure support, and heart medications. Long-term prognosis is poor, as the heart medication does not cure the heart disease; it prevents cardiac disease from getting worse. The exception is when cardiac disease is caused by hyperthyroidism, which often gets better once the hyperthyroidism is treated!
            
    
            
            <br/>5. Cancer
            As dogs and cats live longer, we as veterinarians are seeing more cases of cancer. The most common type of cancer in cats is gastrointestinal cancer, often due to lymphosarcoma. Clinical signs of cancer include:<br/>
            
            Weight loss<br/>
            Not eating<br/>
            Vomiting<br/>
            Diarrhea<br/>
            Difficulty breathing<br/>
            Abdominal distension or bloating<br/>
            Weakness<br/>
            Lethargy<br/>
            Hiding<br/>
            Fever<br/>
            Generalized malaise<br/>
            Once diagnosed, the prognosis for cancer is poor. For this reason, the sooner you notice clinical signs, the sooner diagnosis and treatment may be initiated.

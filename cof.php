        <h1> Would You Like a Cat with That Coffee? Yes Please!</h1>
        <img src="coffee.png" alt="coffee" align="right" height="350px" width="450px">
        <P  style="font-size:25px;"> The Cat Town Café may just sound like your ordinary café, but coffee and lattes aren’t the only thing you’ll find inside. You’ll also see cats! Has California pioneered a new way to get cats adopted? They just may have! 

            Located in Oakland, California, The Cat Town Café & Adoption Center has become the first cat café in the United States which opened in late October, reports Carol Pogash of the New York Times. Inside the café you won’t see cages, but free roaming cats looking for a forever home. 
            
            Patron Eddie Metairie told Pogash that, “Going to a shelter to find a cat in a cage ‘is heartbreaking,’” the Cat Town Café “feels organic.” Metairie ended up adopting a cat from Café. He named her Amélie. 
            
            The Cat Town Café has had 52 successful cat adoptions so far. That’s pretty good for only being open 2 months. 
            
            You may be thinking, “Wouldn’t it be strange for a Café to be overflowing with cats?” Well, yes it might be but fortunately the owners have thought of everything. Only once you’ve ordered your coffee, do you enter the “Cat Zone.” On the other side of the glass doors you’ll see several cats lounging around, all ready to be adopted.
            
            What a great way to spread more awareness about the joys of adoption! ‘Tis the season to adopt from a shelter, or Café!
